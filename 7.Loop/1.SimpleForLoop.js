// Looping with one increment
for(let i = 0; i < 10; i++) {
    console.log("Hello: " + i);
} 

// Print even number: Looping with two increment
for(let i = 0; i < 10; i+=2) {
    console.log("Hello:", i);
} 

// Use loop for timetable
for(let num = 1; num <= 10; num++) {
    console.log(`${num} x 5 = ${num*5}`);
} 

// Print reverse number
for(let i = 10; i > 0; i--) {
    console.log(i);
}